# Odd Or Even

# Ask the user for a number. Depending on whether the number is even or odd, print out an appropriate message to the user. Hint: how does an even / odd number react differently when divided by 2?

# Extras:

# If the number is a multiple of 4, print out a different message.
# Ask the user for two numbers: one number to check (call it num) and one number to divide by (check). If check divides evenly into num, tell that to the user. If not, print a different appropriate message.


def exercise2a():
    number = int(input("Tell me the number: "))

    if number % 4 == 0:
        print("The number " + str(number) + " is even and divides by 4.")
    elif number % 4 != 0 & number % 2 == 0:
        print("The number " + str(number) + " is even.")
    elif number % 2 != 0:
        print("The number " + str(number) + " is odd.")


def exercise2b():
    num = int(input("Tell me the number: "))
    check = int(input("Tell me the modulo: "))

    if num % check == 0:
        print("The number " + str(num) + " divides by " + str(check) + ".")
    elif num % check != 0:
        print("The number " + str(num) + " does not divide by " + str(check) + ".")
